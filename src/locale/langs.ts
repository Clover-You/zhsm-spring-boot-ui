import { zh_CN } from '@/locale/langs/zh_CN'
import { en } from '@/locale/langs/en'
import { LocaleList } from '@/locale/messages'

const LOCALE_LIST: { [key: string]: LocaleList } = { zh_CN, en }

export const loadLocale = (localeName: string) => {
  let locale = en
  for (let key in LOCALE_LIST) {
    const localeInfo = LOCALE_LIST[key]
    if (!localeInfo.name.startsWith(localeName)) continue
    locale = localeInfo
    break
  }
  return locale
}